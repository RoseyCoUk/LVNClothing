// Helper functions
export const getHoodieVariantsByColor = (color: string) => {
  return hoodieVariants.filter(variant => variant.color === color);
};

export const getHoodieVariantsBySize = (size: string) => {
  return hoodieVariants.filter(variant => variant.size === size);
};

export const getHoodieVariant = (color: string, size: string) => {
  return hoodieVariants.find(variant => variant.color === color && variant.size === size);
};

export const getHoodieColors = () => {
  return [...new Set(hoodieVariants.map(variant => variant.color))];
};

export const getHoodieSizes = () => {
  return [...new Set(hoodieVariants.map(variant => variant.size))];
};
